# Coleção ordenada e imutável

# Exemplo de tupla
my_tuple = (1, 2, 2, 3, 4)

print("Tupla:", my_tuple)

# Acessando elementos
print("Primeiro elemento:", my_tuple[0])
print("Último elemento:", my_tuple[-1])

# Método count
count = my_tuple.count(1)
print("O número 1 aparece", count, "vezes na tupla.")

# Método index
index = my_tuple.index(2)
print("O primeiro índice do número 2 é:", index)
