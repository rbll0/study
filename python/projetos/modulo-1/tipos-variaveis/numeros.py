# Examples of different numeric types in Python

# Integer
num_int = 3
print("Inteiro = ", num_int)

# Float
num_float = 3.5
print("Real com ponto flutuante = ", num_float)

# type() function to check the type of the variables
print("Tipo de num_int:", type(num_int))
print("Tipo de num_float:", type(num_float))

#soma + 
soma = 1 + 1
print("Soma = ", soma)

# sub - 
subtracao = 2 - 1
print("Subtração = ", subtracao)

# div / 
divisao = 10 / 2 # resultado sempre float
print("Divisão = ", divisao) # int() para converter para inteiro, se necessário

# mult *
multiplicacao = 2 * 3
print("Multiplicação = ", multiplicacao)

# Mod %
modulo = 10 % 3
print("Módulo = ", modulo)

# div int //
divisao = 10 // 2 
print("Divisão = ", divisao) # int() para converter para inteiro, se necessário
